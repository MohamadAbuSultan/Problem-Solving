
#include <iostream>

using namespace std;

int main()
{
	long long number1, number2;
	cin >> number1 >> number2;
	cout << number1 << " + " << number2 << " = " << number1 + number2 << endl;
	cout << number1 << " * " << number2 << " = " << number1 * number2 << endl;
	cout << number1 << " - " << number2 << " = " << number1 - number2 << endl;
}
